                    //log.trace "Got JPEG IMAGE"
                    child?.log "Got JPEG IMAGE"
                }
            }*/

            if (ret.data) {
                //log.trace "RESPONSE>" + ret.data
                //child?.log "RESPONSE>" + ret.data
                response = ret.data
            } else {
                log.error "No response to query: $ret.data"
                child?.log "No response to query: $ret.data", "error"
            }
        }
    } catch (e) {
